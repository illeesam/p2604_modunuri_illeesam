/**
 * ═══════════════════════════════════════════════════════════════════════════
 *  PostCSS 설정 파일
 * ═══════════════════════════════════════════════════════════════════════════
 *
 *  ▣ 이 파일이 왜 필요한가?
 *    - Tailwind CLI 내부적으로 PostCSS를 호출하여 CSS를 처리합니다.
 *    - PostCSS는 여러 플러그인을 순차 적용하는 CSS 변환 도구입니다.
 *    - 브라우저는 이 파일을 읽지 않습니다. 빌드 타임 도구용입니다.
 *
 *  ▣ 구성 플러그인:
 *    1. tailwindcss   — Tailwind 유틸리티 CSS 생성
 *    2. autoprefixer  — 구형 브라우저용 벤더 프리픽스 자동 추가
 *                       (-webkit-, -moz-, -ms- 등)
 *
 *  ▣ 없어도 동작하나요?
 *    - Tailwind CLI는 이 파일이 없어도 기본 동작은 합니다.
 *    - 단, autoprefixer가 빠져 Safari/구형 모바일 호환성이 떨어질 수 있습니다.
 *    - 타겟 브라우저가 최신 크롬/사파리만이라면 생략 가능합니다.
 * ═══════════════════════════════════════════════════════════════════════════
 */
module.exports = {
  plugins: {
    tailwindcss:  {},
    autoprefixer: {},
  },
};
